    <h2>Profil</h2>
    <p>Kgaards er et design firma der udvikler og markedsfører
    produkter inden for belysning. Firmaet er ejet af Civilingeniør
    Thorbjørn R. Kaagaard, der i 10 år har arbejdet med udvikling og
    produktdesign og igennem Kgaards nu arbejder med egne ideer.</p>

    <h2>Værdigrundlag</h2>
    <p>Kgaards vil til enhver tid tilstræbe kvalitet og innovation,
    både i forhold til produkter og materialer og når det
    gælder kommunikation, samarbejdspartnere og kunder.</p>

    <h2>Målsætning</h2>
    <p>
    At levere kvalitetsprodukter og lade den gode ide være den bestemmende drivkraft bag
    produktudviklingen. At give Kgaards kunder oplevelsen af at “her er tænkt” over tingene.</p>
